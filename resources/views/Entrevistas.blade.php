        <h2 class="text-2xl font-bold">¿Qué hay que saber antes de presentarse a una entrevista de trabajo?</h2>

        <h3 class="text-xl font-semibold pt-3 pb-2">Tipos de entrevista</h3>
        <ul class="ml-10 pt-1" >
            <li class="text-xl">Individual. Amplía la información del curriculum y profundiza en tus aspectos y cualidades personales. Lo más común es que la realice un entrevistador pero, en ocasiones, puede haber más de uno. Dentro de este tipo puedes encontrarte con distintas modalidades según su finalidad: preselección o fases avanzadas de selección.</li>
            <li class="text-xl">Colectiva. Suele comenzar con la proposición de un tema o una tarea. Posteriormente, el grupo en el que estarás incluido deberá discutir hasta llegar a una postura de acuerdo común. Este tipo de entrevista se utiliza para evaluar la capacidad de trabajo en grupo, la iniciativa, la seguridad, el liderazgo, la capacidad de comunicación, y la actitud de escucha hacia los demás.</li>
        </ul>
        <h3 class="text-xl font-semibold pt-3 pb-2">Preparación de la entrevista</h3>
        <p class="nivelp">Es una situación que produce un alto grado de nerviosismo y ansiedad. Puedes prepararla de antemano y afrontar la entrevista como un reto enriquecedor, una situación de la que puedes aprender y obtener aspectos positivos. Para esto puedes seguir los siguientes consejos que te proponemos:</p>
        <ul class="ml-10 pt-2" >
            <li class="text-xl">Infórmate previamente sobre el puesto, la empresa y el cargo.</li>
            <li class="text-xl">Identifica tus puntos fuertes y débiles. Ten en cuenta que tampoco te convienen desvelar debilidades que puedan perjudicarte.
            <li class="text-xl">Analiza tu proyecto y tus argumentos.</li>
            <li class="text-xl">Prepárate las preguntas que vas a formular.</li>
            <li class="text-xl">Sé optimista y confía en ti mismo.</li>
            <li class="text-xl">Analiza la imagen que quieres transmitir y elige, en función de ella, los elementos de tu aspecto personal por si en algún momento de la entrevista lo necesitas.</li>
            <li class="text-xl">Prepara tu curriculum, documentos acreditativos y referencias de forma ordenada y limpia.</li>
            <li class="text-xl">Sé puntual.</li>
        </ul>
        <p class="pt-2">Debes considerar que el proceso de pasar por una entrevista, incluso aunque no seas seleccionado, supone una experiencia positiva que te aporta seguridad para afrontar nuevas entrevistas como situaciones más conocidas.</p>
        <h3 class="text-xl font-semibold pt-3 pb-2">¿Qué te pueden preguntar?</h3>
        <p>No dejes un momento para la improvisación. Durante la entrevista, te preguntarán acerca de diversos ámbitos: procura reflexionar antes pensando qué podrías contestar.</p>
        <ul class="ml-10 pt-2" >
            <li class="text-xl">Personalidad: sobre ti mismo, anécdotas, valores, motivaciones e influencias.</li>
            <li class="text-xl">Formación: razones para la elección de tu itinerario de formación.</li>
            <li class="text-xl">Trabajos anteriores, actividades, funciones, proyectos, compañeros.</li>
            <li class="text-xl">Empleo: preferencias, relaciones en el trabajo, puntos fuertes y débiles, objetivos.</li>
            <li class="text-xl">Vida privada: familia, relaciones personales, tiempo libre, vacaciones, hobbies.</li>
            <li class="text-xl">Varias: incorporación al trabajo, opiniones, referencias.</li>
        </ul>
        <p>Si quieres prepararte para una entrevista, prueba a contestar preguntas como:</p>
        <ul class="ml-10 pt-2" >
            <li class="text-xl">¿Por qué dejó su último trabajo?</li>
            <li class="text-xl">¿Qué conoce sobre nuestra empresa?</li>
            <li class="text-xl">Nombre dos de sus puntos fuertes y débiles.</li>
            <li class="text-xl">¿Dónde se ve en cinco años?</li>
            <li class="text-xl">¿Por qué debería contratarlo?</li>
            <li class="text-xl">Describa un día habitual en su anterior trabajo.</li>
            <li class="text-xl">¿Qué puede aportar a la empresa?</li>
        </ul>
        <p>Las respuestas deben ser concisas y directas. Siempre que puedas, utiliza ejemplos para ser más claro y trata de buscar la relación con temas laborales.</p>
        <p>Además de preparar las respuestas a las posibles preguntas, puedes preparar preguntas para formular tú mismo acerca de:</p>
        <ul class="ml-10 pt-2" >
            <li class="text-xl">Principales responsabilidades y funciones del puesto.</li>
            <li class="text-xl">Desarrollo profesional, condiciones, incentivos.</li>
            <li class="text-xl">Sobre tus futuros compañeros, jefes, subordinados.</li>
        </ul>
